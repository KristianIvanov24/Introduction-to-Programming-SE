
#include <iostream>
/*
	Задача 3 : Да се напише програма, която въвежда от клавиатурата три цели числа x, y, z и ги сортира във възходящ ред.

	Вход: 31 15 5, Изход: 5 15 31

	Вход: -4 -19 -10, Изход: -19 -10 -4
*/

int main()
{
	int x, y, z;
	std::cin >> x >> y >> z;

	if (x > y)
	{
		x += y;
		y = x - y;
		x -= y;
	}

	if (x > z)
	{
		x += z;
		z = x - z;
		x -= z;
	}

	if (y > z)
	{
		y += z;
		z = y - z;
		y -= z;
	}

	std::cout << x << ' ' << y << ' ' << z;
	return 0;
}
